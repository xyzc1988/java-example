package com.etoak.bean;

import java.util.Date;

public class Person {
	private String name;
	private Date age;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getAge() {
		return age;
	}
	public void setAge(Date age) {
		this.age = age;
	}
	

}
